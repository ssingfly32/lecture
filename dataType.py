# 파이썬에서 사용 가능한 기본 데이터 타입

# 숫자형
a = 123
print(type(a)) # <class 'int'> : 정수형

b = 3.14
print(type(b)) # <class 'float'> : 실수형

# 불리언
c = True
d = False
print(type(c)) # <class 'bool'> : boolean
print(type(d))  # <class 'bool'> : boolean

# 문자열형
str = "hello"
sty1 = '대한민국'

print (type(str)) # <class'str'> : 문자열

print(None) # null

# 이 외에도 리스트, 튜플, 딕셔너리 타입 등이 있다.
