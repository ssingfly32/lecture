// 하나의 자바 프로젝트에는 반드시 프로젝트이름과 같은 이름의 public한 클래스가 존재해야 한다.
public class HelloJava { // HelloJava 클래스의 시작, 앞으로 컨트롤 스페이스바로 빠르게 치자
	public static void main(String[] args) { // main 메서드(프로그램의 진입점)
		System.out.println("Hello, Java");
		System.out.println("배고파~");
	} // main 메서드 끝
	
	
	
}// HelloJava 클래스의 끝
